static SPI_HandleTypeDef *_hspi;

static void spi_write_byte(uint8_t data)
{
    HAL_SPI_Transmit(_hspi, &data, 1, HAL_MAX_DELAY);
}

static void write_cmd(uint8_t cmd)
{
    LCD_DC_CMD();
    LCD_CS_LOW();
    spi_write_byte(cmd);
    LCD_CS_HIGH();
}

static void write_data(uint8_t data)
{
    LCD_DC_DATA();
    LCD_CS_LOW();
    spi_write_byte(data);
    LCD_CS_HIGH();
}

static void set_address_window(uint16_t x0, uint16_t y0, uint16_t x1, uint16_t y1)
{
    write_cmd(0x2A); /* Column address set */
    write_data(x0 >> 8); write_data(x0 & 0xFF);
    write_data(x1 >> 8); write_data(x1 & 0xFF);

    write_cmd(0x2B); /* Row address set */
    write_data(y0 >> 8); write_data(y0 & 0xFF);
    write_data(y1 >> 8); write_data(y1 & 0xFF);

    write_cmd(0x2C); /* Memory write */
}

void ILI9341_Init(SPI_HandleTypeDef *hspi)
{
    LOG_DEBUG("ILI9341: init start");

    _hspi = hspi;

    /* CS and DC are GPIO outputs — deselect initially */
    LCD_CS_HIGH();
    LCD_DC_DATA();

    HAL_Delay(10);

    write_cmd(0x01); /* Software reset */
    HAL_Delay(120);

    write_cmd(0xCF); /* Power control B */
    write_data(0x00); write_data(0xC1); write_data(0x30);

    write_cmd(0xED); /* Power on sequence */
    write_data(0x64); write_data(0x03); write_data(0x12); write_data(0x81);

    write_cmd(0xE8); /* Driver timing A */
    write_data(0x85); write_data(0x00); write_data(0x78);

    write_cmd(0xCB); /* Power control A */
    write_data(0x39); write_data(0x2C); write_data(0x00);
    write_data(0x34); write_data(0x02);

    write_cmd(0xF7); /* Pump ratio */
    write_data(0x20);

    write_cmd(0xEA); /* Driver timing B */
    write_data(0x00); write_data(0x00);

    write_cmd(0xC0); /* Power control 1 */
    write_data(0x23);

    write_cmd(0xC1); /* Power control 2 */
    write_data(0x10);

    write_cmd(0xC5); /* VCOM control 1 */
    write_data(0x3E); write_data(0x28);

    write_cmd(0xC7); /* VCOM control 2 */
    write_data(0x86);

    write_cmd(0x36); /* Memory access control — landscape mode */
    write_data(0xE8); /* MY | MX | MV | BGR. At this MADCTL value, characters render
                          individually rotated 180° (string order is correct) --
                          compensated for in ILI9341_DrawChar's pixel iteration order. */

    write_cmd(0x3A); /* Pixel format: 16-bit RGB565 */
    write_data(0x55);

    write_cmd(0xB1); /* Frame rate: ~60 Hz */
    write_data(0x00); write_data(0x18);

    write_cmd(0xB6); /* Display function control */
    write_data(0x08); write_data(0x82); write_data(0x27);

    write_cmd(0xF2); /* 3G disable */
    write_data(0x00);

    write_cmd(0x26); /* Gamma set */
    write_data(0x01);

    write_cmd(0xE0); /* Positive gamma correction */
    write_data(0x0F); write_data(0x31); write_data(0x2B); write_data(0x0C);
    write_data(0x0E); write_data(0x08); write_data(0x4E); write_data(0xF1);
    write_data(0x37); write_data(0x07); write_data(0x10); write_data(0x03);
    write_data(0x0E); write_data(0x09); write_data(0x00);

    write_cmd(0xE1); /* Negative gamma correction */
    write_data(0x00); write_data(0x0E); write_data(0x14); write_data(0x03);
    write_data(0x11); write_data(0x07); write_data(0x31); write_data(0xC1);
    write_data(0x48); write_data(0x08); write_data(0x0F); write_data(0x0C);
    write_data(0x31); write_data(0x36); write_data(0x0F);

    write_cmd(0x11); /* Sleep out */
    HAL_Delay(120);

    write_cmd(0x29); /* Display on */
    HAL_Delay(20);

    LOG_DEBUG("ILI9341: init done");
}

void ILI9341_FillRect(uint16_t x, uint16_t y, uint16_t w, uint16_t h, uint16_t color)
{
    LOG_TRACE("ILI9341: FillRect x=%u y=%u w=%u h=%u color=0x%04X", x, y, w, h, color);

    set_address_window(x, y, x + w - 1, y + h - 1);

    uint8_t hi = color >> 8;
    uint8_t lo = color & 0xFF;

    LCD_DC_DATA();
    LCD_CS_LOW();
    for (uint32_t i = 0; i < (uint32_t)w * h; i++) {
        spi_write_byte(hi);
        spi_write_byte(lo);
    }
    LCD_CS_HIGH();
}

void ILI9341_FillScreen(uint16_t color)
{
    ILI9341_FillRect(0, 0, ILI9341_WIDTH, ILI9341_HEIGHT, color);
}

void ILI9341_DrawChar(uint16_t x, uint16_t y, char c, uint16_t fg, uint16_t bg)
{
    if (c < 0x20 || c > 0x7E)
        c = '?';

    const uint8_t *glyph = font8x8[c - 0x20];

    set_address_window(x, y, x + 7, y + 7);

    uint8_t fg_hi = fg >> 8, fg_lo = fg & 0xFF;
    uint8_t bg_hi = bg >> 8, bg_lo = bg & 0xFF;

    /* Row order is reversed relative to the natural bitmap scan (row 0
     * first) to cancel out the vertical component of this panel's MADCTL
     * setting -- see the comment on the 0x36 command in ILI9341_Init.
     * Column order (MSB/leftmost bit first) is the natural, uncompensated
     * scan -- reversing it previously overcorrected into a horizontal
     * mirror. */
    LCD_DC_DATA();
    LCD_CS_LOW();
    for (int row = 7; row >= 0; row--) {
        uint8_t bits = glyph[row];
        for (int col = 7; col >= 0; col--) {
            if (bits & (1 << col)) {
                spi_write_byte(fg_hi);
                spi_write_byte(fg_lo);
            } else {
                spi_write_byte(bg_hi);
                spi_write_byte(bg_lo);
            }
        }
    }
    LCD_CS_HIGH();
}

void ILI9341_DrawString(uint16_t x, uint16_t y, const char *str, uint16_t fg, uint16_t bg)
{
    LOG_TRACE("ILI9341: DrawString \"%s\" at (%u,%u)", str, x, y);

    /* Increasing X moves the draw position left on this panel (see the
     * MADCTL comment in ILI9341_Init), so advancing left-to-right on
     * screen means decreasing X here. */
    while (*str) {
        ILI9341_DrawChar(x, y, *str++, fg, bg);
        if (x < 8) {
            x = ILI9341_WIDTH - 8;
            y += 8;
        } else {
            x -= 8;
        }
    }
}
